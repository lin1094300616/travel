    @TableId(value = "comment_id", type = IdType.AUTO)
    private Integer commentId;

    /**
     * 评论名称
     */
    private String commentName;

    /**
     * 评论内容
     */
    private String commentContent;

    /**
     * 评分
     */
    private Integer score;

    /**
     * 评论类型
     */
    private String type;

    /**
     * 用户ID
     */
    private Integer userId;

